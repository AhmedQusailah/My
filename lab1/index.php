<?php

echo "<pre>";
echo "=========== دوال النصوص (String Functions) ===========\n\n";

/*--------------------------------------------------------
1) strlen
   ترجع طول النص بعدد الحروف.
--------------------------------------------------------*/
echo "1) strlen\n";
echo "طول النص: " . strlen("Ahmed") . "\n\n";

/*--------------------------------------------------------
2) strrev
   تعكس النص.
--------------------------------------------------------*/
echo "2) strrev\n";
echo "عكس النص: " . strrev("Hello") . "\n\n";

/*--------------------------------------------------------
3) strtolower
   تحويل كل الحروف إلى صغيرة.
--------------------------------------------------------*/
echo "3) strtolower\n";
echo strtolower("PHP IS GREAT") . "\n\n";

/*--------------------------------------------------------
4) strtoupper
   تحويل كل الحروف إلى كبيرة.
--------------------------------------------------------*/
echo "4) strtoupper\n";
echo strtoupper("php is great") . "\n\n";

/*--------------------------------------------------------
5) ucfirst
   تجعل أول حرف من الجملة كبير.
--------------------------------------------------------*/
echo "5) ucfirst\n";
echo ucfirst("hello world") . "\n\n";

/*--------------------------------------------------------
6) ucwords
   تجعل أول حرف من كل كلمة كبير.
--------------------------------------------------------*/
echo "6) ucwords\n";
echo ucwords("hello world from php") . "\n\n";

/*--------------------------------------------------------
7) trim
   تحذف المسافات من بداية ونهاية النص.
--------------------------------------------------------*/
echo "7) trim\n";
echo trim("   Ahmed   ") . "\n\n";

/*--------------------------------------------------------
8) str_replace
   استبدال جزء من النص بقيمة أخرى.
--------------------------------------------------------*/
echo "8) str_replace\n";
echo str_replace("world", "PHP", "Hello world") . "\n\n";

/*--------------------------------------------------------
9) strpos
   إيجاد موقع كلمة داخل النص.
--------------------------------------------------------*/
echo "9) strpos\n";
echo "موقع كلمة world: " . strpos("Hello world", "world") . "\n\n";

/*--------------------------------------------------------
10) substr
    قص جزء من النص.
--------------------------------------------------------*/
echo "10) substr\n";
echo substr("Hello PHP", 6) . "\n\n";



echo "\n=========== دوال المصفوفات (Array Functions) ===========\n\n";

/*--------------------------------------------------------
1) count
   عدد عناصر المصفوفة.
--------------------------------------------------------*/
echo "1) count\n";
$arr = [1,2,3,4];
echo "عدد العناصر: " . count($arr) . "\n\n";

/*--------------------------------------------------------
2) array_push
   إضافة عنصر إلى آخر المصفوفة.
--------------------------------------------------------*/
echo "2) array_push\n";
array_push($arr, 5);
print_r($arr);
echo "\n";

/*--------------------------------------------------------
3) array_pop
   حذف آخر عنصر.
--------------------------------------------------------*/
echo "3) array_pop\n";
array_pop($arr);
print_r($arr);
echo "\n";

/*--------------------------------------------------------
4) array_shift
   حذف أول عنصر.
--------------------------------------------------------*/
echo "4) array_shift\n";
array_shift($arr);
print_r($arr);
echo "\n";

/*--------------------------------------------------------
5) array_unshift
   إضافة عنصر في بداية المصفوفة.
--------------------------------------------------------*/
echo "5) array_unshift\n";
array_unshift($arr, 10);
print_r($arr);
echo "\n";

/*--------------------------------------------------------
6) in_array
   التحقق من وجود قيمة داخل المصفوفة.
--------------------------------------------------------*/
echo "6) in_array\n";
echo in_array(10, $arr) ? "موجود\n\n" : "غير موجود\n\n";

/*--------------------------------------------------------
7) array_merge
   دمج مصفوفتين.
--------------------------------------------------------*/
echo "7) array_merge\n";
$a = [1,2];
$b = [3,4];
print_r(array_merge($a, $b));
echo "\n";